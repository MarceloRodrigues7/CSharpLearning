using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace KeyPress
{
    public class Calculo
    {
        public decimal Adicao(decimal valorUm, decimal valorDois) => valorUm + valorDois;


        public decimal Subtracao(decimal valorUm, decimal valorDois) => valorUm - valorDois;

        public decimal Multiplicacao(decimal valorUm, decimal valorDois) => valorUm * valorDois;

        public decimal Divisao(decimal valorUm, decimal valorDois) => valorUm / valorDois;
    }
}
